int main(){		
	printf("number : %d\n", number);			// In ra gia tri cua bien 'number' kieu du lieu int
	printf("kt : %c\n", kt);						// In ra gia tri cua bien 'kt' kieu du lieu char
	return 0;									//tra ve gia tri 0 de ket thuc chuong trinh
}






